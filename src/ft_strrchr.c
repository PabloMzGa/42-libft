#include "../include/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char			*last;
	unsigned char	uc;

	uc = (unsigned char)c;
	last = NULL;
	while (*s)
	{
		if (*s == uc)
			last = (char *)s;
		s++;
	}
	if (uc == '\0')
		return ((char *)s);
	return (last);
}
